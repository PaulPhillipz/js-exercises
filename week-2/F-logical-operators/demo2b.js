var num = 10;

console.log(num > 5 && num < 15) // should give true

console.log(num < 10 || num === 10) // should give true

console.log(false || true) // should give true

console.log(!true) // should give false

var greaterThan5 = false;
console.log(greaterThan5 = num > 5) // should give true

console.log(!greaterThan5) // should give false

console.log(!(num === 10)) // should give false